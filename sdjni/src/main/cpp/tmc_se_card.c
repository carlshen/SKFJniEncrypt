	buf[3] = (u8)0x80;//free

	//delete AC
	buf[4] = (u8)0x40;//forbidden

	//set Attribute
	buf[6] = 0xFF;

	//SFI
	buf[7] = 0x81;

	//set EF FileType
	buf[8] = 0x01;//binary file

	//set EF Size
	buf[9] = (u8)(size >> 8);
	buf[10] = (u8)(size & 0x00FF);

	apdu->lc = 0x14;
	apdu->data = buf;
	apdu->datalen = apdu->lc;

